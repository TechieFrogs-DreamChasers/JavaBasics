public class SwitchDemo {
    public static void main(String[] args) {
        int i = 10;

        switch(i){
            case 1:
            System.out.println("In case 1");
            break;
            case 2:
            System.out.println("In case 2");
            break;
            case 10:
            System.out.println("In case 10");
            break;
            default:
            System.out.println("In Default");

        }
    }
    
}

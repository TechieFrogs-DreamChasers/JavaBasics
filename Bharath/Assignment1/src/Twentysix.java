public class Twentysix {
    public static void main(String[] args) {
        int count = 1;

        for(int i1=1; i1<=4; i1++){
            for(int j=1; j<=i1; j++){
                System.out.print(count+" ");
                count++;
            }   
            System.out.println();
        }

    }
    
}
